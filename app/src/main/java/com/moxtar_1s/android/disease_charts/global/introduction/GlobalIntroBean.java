package com.moxtar_1s.android.disease_charts.global.introduction;

public class GlobalIntroBean {
    private String existingConfirmed;
    private String confirmed;
    private String dead;
    private String cured;
    private String date;

    public void setExistingConfirmed(String existingConfirmed) {
        this.existingConfirmed = existingConfirmed;
    }

    public String getExistingConfirmed() {
        return existingConfirmed;
    }

    public void setConfirmed(String confirmed) {
        this.confirmed = confirmed;
    }

    public String getConfirmed() {
        return confirmed;
    }

    public void setDead(String dead) {
        this.dead = dead;
    }

    public String getDead() {
        return dead;
    }

    public void setCured(String cured) {
        this.cured = cured;
    }

    public String getCured() {
        return cured;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDate() {
        return date;
    }
}
