package com.moxtar_1s.android.disease_charts.china.introduction;

public class ChinaIntroBean {
    private String existingConfirmed;
    private String noSymptom;
    private String input;
    private String confirmed;
    private String dead;
    private String cured;
    private String date;

    public void setExistingConfirmed(String existingConfirmed) {
        this.existingConfirmed = existingConfirmed;
    }

    public String getExistingConfirmed() {
        return existingConfirmed;
    }

    public void setNoSymptom(String noSymptom) {
        this.noSymptom = noSymptom;
    }

    public String getNoSymptom() {
        return noSymptom;
    }

    public void setInput(String input) {
        this.input = input;
    }

    public String getInput() {
        return input;
    }

    public void setConfirmed(String confirmed) {
        this.confirmed = confirmed;
    }

    public String getConfirmed() {
        return confirmed;
    }

    public void setDead(String dead) {
        this.dead = dead;
    }

    public String getDead() {
        return dead;
    }

    public String getCured() {
        return cured;
    }

    public void setCured(String cured) {
        this.cured = cured;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDate() {
        return date;
    }
}
